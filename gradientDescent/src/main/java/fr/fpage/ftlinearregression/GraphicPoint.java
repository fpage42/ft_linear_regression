package fr.fpage.ftlinearregression;

public class GraphicPoint {

    protected float value1, value2;

    public GraphicPoint(float value1, float value2) {
        this.value1 = value1;
        this.value2 = value2;
    }

    public float getValue1() {
        return value1;
    }

    public float getValue2() {
        return value2;
    }
}
